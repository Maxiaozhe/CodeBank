SELECT 
 COLUMN_NAME AS "列名"
,CASE WHEN DATA_TYPE IN('VARCHAR2','CHAR') THEN DATA_TYPE || '(' || DATA_LENGTH || ')'
WHEN DATA_TYPE='NUMBER' AND NVL(DATA_PRECISION,0)=0 AND NVL(DATA_SCALE,0)=0 THEN DATA_TYPE
WHEN DATA_TYPE='NUMBER' THEN DATA_TYPE || '(' || TO_CHAR( DATA_PRECISION) || ',' || TO_CHAR(DATA_SCALE) || ')'
ELSE  DATA_TYPE END AS "データ型"
FROM 
ALL_TAB_COLUMNS 
WHERE 
(OWNER = :OWNER) 
AND (TABLE_NAME = :TABLENAME) 
ORDER BY
OWNER, 
TABLE_NAME, 
COLUMN_ID